package com.zuwo.data;

public class Appointment {
	
	private double rent;
	private double counter_fee;
	private double fact_rent;
	
	public double getRent() {
		return rent;
	}
	public void setRent(double rent) {
		this.rent = rent;
	}
	public double getCounter_fee() {
		return counter_fee;
	}
	public void setCounter_fee(double counter_fee) {
		this.counter_fee = counter_fee;
	}
	public double getFact_rent() {
		return fact_rent;
	}
	public void setFact_rent(double fact_rent) {
		this.fact_rent = fact_rent;
	}
	
}
